package polymorphism;

public class LgTV {
	
	
	public void powerOn() {
		System.out.println("LgTV-전원키기");
	}
	public void powerOff() {
		System.out.println("LgTV-전원끄기");
	}
	public void soundUp() {
		System.out.println("LgTV-소리키우기");
	}
	public void soundDown() {
		System.out.println("LgTV-소리줄이기");
	}
}
